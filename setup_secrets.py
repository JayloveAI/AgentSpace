"""
AgentSpace 一键配置脚本
===================
读取 SECRETS.env，自动写入所有组件的配置文件。

用法:
    python setup_secrets.py
"""
import os
import sys
from pathlib import Path

ROOT = Path(__file__).parent


def parse_secrets(env_file: Path) -> dict:
    """解析 .env 文件为字典"""
    secrets = {}
    if not env_file.exists():
        print(f"[ERROR] {env_file} 不存在！")
        sys.exit(1)

    for line in env_file.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" in line:
            key, _, value = line.partition("=")
            # 去掉行尾注释
            if " # <=" in value:
                value = value.split(" # <=")[0]
            secrets[key.strip()] = value.strip()

    return secrets


def check_required(secrets: dict) -> list[str]:
    """检查必填项，返回未填写的列表"""
    required = {
        "HUB_SERVER_IP": "服务器公网 IP",
        "GLM_API_KEY": "智谱 AI API Key (或 OPENAI_API_KEY)",
        "HUB_JWT_SECRET": "JWT 签名密钥",
        "FRP_SERVER_ADDR": "FRP 服务器地址",
        "FRP_TOKEN": "FRP 认证 Token",
        "POSTGRES_PASSWORD": "PostgreSQL 密码",
        "FRP_DASHBOARD_PASSWORD": "FRP Dashboard 密码",
    }
    missing = []
    for key, desc in required.items():
        val = secrets.get(key, "")
        if not val:
            if key == "GLM_API_KEY" and secrets.get("OPENAI_API_KEY"):
                continue
            missing.append(f"  {key} — {desc}")
    return missing


