def main():
    print("=" * 60)
    print("  AgentSpace 一键配置")
    print("=" * 60)

    # 1. 读取
    secrets_file = ROOT / "SECRETS.env"
    print(f"\n[1/4] 读取 {secrets_file}")
    secrets = parse_secrets(secrets_file)
    print(f"  已加载 {len(secrets)} 个配置项")

    # 2. 校验
    print("\n[2/4] 检查必填项")
    missing = check_required(secrets)
    if missing:
        print("  以下必填项未配置：")
        for m in missing:
            print(m)
        print(f"\n  请编辑 {secrets_file} 后重新运行")
        sys.exit(1)
    print("  所有必填项已配置 [OK]")

    # 3. 分发
    print("\n[3/4] 分发配置到各组件")
    write_hub_env(secrets)
    write_client_env(secrets)
    write_package_env(secrets)
    write_docker_env(secrets)

    # 4. 环境变量
    print("\n[4/4] 设置当前进程环境变量")
    count = 0
    for key, value in secrets.items():
        if value:
            os.environ[key] = value
            count += 1
    print(f"  已设置 {count} 个环境变量")

    print("\n" + "=" * 60)
    print("  [OK] 配置完成！")
    print("=" * 60)
    print(f"\n  启动服务端: cd hub && python -m uvicorn hub_server.main:app --reload")
    print(f"  启动客户端: cd hub && python -m client_sdk.cli start")
    print(f"  Docker:     cd hub && docker-compose --env-file .env.docker up -d")


if __name__ == "__main__":
    main()
